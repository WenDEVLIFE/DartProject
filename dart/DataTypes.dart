void main(){
  int num = 100; // integer
  double percent = 99.99; // double
  String name = 'Furina de Fontaine';   // string
  bool isTrue = true; // boolean
  print('num: $num'); // string interpolation
  print('percent: $percent'); // string interpolation
  print('name: $name'); // string interpolation
  print('isTrue: $isTrue'); // string interpolation
}